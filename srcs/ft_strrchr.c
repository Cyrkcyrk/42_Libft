#include "ftlib.h"

char *ft_strrchr(const char *s, int c)
{
	char *pos, *ret;
	
	ret = 0;
	pos = s-1;
	while(*(++pos) != '\0')
		if(*pos == c)
			ret = pos;
	return (ret);
}
